package edu.grinnell.psychMAP26.AGLV2;

/**
 *
 * @author fui
 *
 * A generic tuple with two elements.
 */
public class Pair<A, B> {
    public final A first;
    public final B second;

    public Pair(A first, B second) {
        this.first = first;
        this.second = second;
    }

    @Override
    public String toString() {
        return "(" + first + ", " + second + ")";
    }
}

